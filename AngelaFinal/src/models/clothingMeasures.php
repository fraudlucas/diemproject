<?php

class CloathingMeasures{

	private $idCloathingMeasures;
	private $sizeCloaths;

	public function getIdCloathingMeasures()
	{
	    return $this->idCloathingMeasures;
	}
	
	public function setIdCloathingMeasures($idCloathingMeasures)
	{
	    return $this->idCloathingMeasures = $idCloathingMeasures;
	}
	public function getSizeCloaths()
	{
	    return $this->sizeCloaths;
	}
	
	public function setSizeCloaths($sizeCloaths)
	{
	    return $this->sizeCloaths = $sizeCloaths;
	}

}



?>